def get_cac(clinic_type: str) -> int:
    return CAC_BY_CLINIC.get(clinic_type, 50000)


def get_elasticity(clinic_type: str) -> float:
    return PRICE_ELASTICITY.get(clinic_type, 0.5)


def get_doctor_capacity(clinic_type: str) -> int:
    return DOCTOR_DAILY_CAPACITY.get(clinic_type, 40)


def get_recommended_staff(clinic_type: str) -> Dict[str, int]:
    return RECOMMENDED_STAFF.get(clinic_type, {"간호사": 1, "행정/원무": 1})


def get_labor_cost(role: str, experience: str = "3년차", region_code: str = "11") -> int:
    """월 급여 (원 단위)."""
    role_data = LABOR_COST_MONTHLY.get(role, {})
    base = role_data.get(experience, role_data.get("3년차", 300)) * 10000
    factor = LABOR_REGION_FACTOR.get(region_code[:2], 1.0)
    return int(base * factor)


def total_labor_cost_with_overhead(monthly_salary: int) -> int:
    """4대보험+퇴직금 포함 실비용."""
    return int(monthly_salary * (1 + LABOR_OVERHEAD_RATIO))
